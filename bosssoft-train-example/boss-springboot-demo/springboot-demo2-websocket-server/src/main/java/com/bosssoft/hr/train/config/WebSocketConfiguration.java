import org.springframework.web.socket.server.standard.ServerEndpointExporter;


/**
 * @description:
 * @author:zyz
 * @time:2020/6/9--8:58
 */
@Configuration
public class WebSocketConfiguration {

    @Bean
    public ServerEndpointExporter serverEndpointExporter() {
        return new ServerEndpointExporter();
    }

}
